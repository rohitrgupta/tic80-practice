function box(x,y, idx)
 if idx <13 then
		rect(x*27, y *27, 27, 27, idx+1)
	else
		rect(x*27, y *27, 27, 27, 13)
	end	
	spr((idx-1)*3, x*27+2, y*27+11,0)
	spr((idx-1)*3+1, x*27+10, y*27+11,0)	 
	spr((idx-1)*3+2, x*27+18, y*27+11,0)
end


function grid(gape, till)
	for x = 0, till, gape do
 	line(x,0,x, till,1)
  line(0,x,till, x,1)
 end
end

